void lowpass()
{
   int i, j;
   int a1[100][100], a2[100][100], a3[100][100];
   int a4[100][100], a5[100][100], a6[100][100];
   int a7[100][100], a8[100][100], a9[100][100];
   int a[200][200], temp1[200][200], temp2[200][200];

   for (i=1; i<100; i+=1) {
     for (j=1; j<100; j+=1) {
        temp1[i][j] = 9 * a1[i-1][j-1] + a2[i-1][j]  + 2 * a3[i-1][j+1];
     }
   }

   for (i=1; i<100; i+=1) {
     for (j=1; j<100; j+=1) {
        temp2[i][j] = 3 * a4[i][j-1] + 4 * a5[i][j] + 5 * a6[i][j+1];
     }
   }

   for (i=1; i<20; i+=1) {
     for (j=1; j<100; j+=1) {
        a[i][j] = temp1[i][j] + temp2[i][j] + 6 * a7[i+1][j-1] + 7 * a8[i+1][j] + 8 * a9[i+1][j+1];
	 }
   }

   for (i=20; i<100; i+=1) {
     for (j=1; j<100; j+=1) {
        a[i][j] = temp1[i][j] + temp2[i][j] + 2 * (3 * a7[i+1][j-1] + 4 * a9[i+1][j+1]) + 7 * a8[i+1][j] ;
	 }
   }
 }
